package beans;

public class Major
{
    private int id;
    private String majorName;
    
    public int getId() {
        return this.id;
    }
    
    public void setId(final int id) {
        this.id = id;
    }
    
    public String getMajorName() {
        return this.majorName;
    }
    
    public void setMajorName(final String majorName) {
        this.majorName = majorName;
    }
}
