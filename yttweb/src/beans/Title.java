package beans;

public class Title
{
    private int id;
    private String titleName;
    
    public long getId() {
        return this.id;
    }
    
    public void setId(final int id) {
        this.id = id;
    }
    
    public String getTitleName() {
        return this.titleName;
    }
    
    public void setTitleName(final String titleName) {
        this.titleName = titleName;
    }
}
